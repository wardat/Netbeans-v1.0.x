package org.netbeans.core.windows;

/** Specifies default programmatic names for default workspaces
8 presented in the system.
*
* @author Dafe Simonek
*/
public interface WellKnownWorkspaceNames {

    /** Name of the editing workspace */
    public static final String EDITING = "Editing"; // NOI18N
    /** Name of the gui editing workspace */
    public static final String VISUAL = "Visual"; // NOI18N
    /** Name of the browsing workspace */
    public static final String BROWSING = "Browsing"; // NOI18N
    /** Name of the running workspace */
    public static final String RUNNING = "Running"; // NOI18N
    /** Name of the debugging workspace */
    public static final String DEBUGGING = "Debugging"; // NOI18N

}

