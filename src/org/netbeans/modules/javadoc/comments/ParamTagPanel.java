    void setData( JavaDocTag tag ) {
        parameterComboBox.getEditor().setItem( ((JavaDocTag.Param)tag).parameterName() );
        descriptionTextArea.setText( ((JavaDocTag.Param)tag).parameterComment() );
        descriptionTextArea.setCaretPosition( 0 );
    }

    JavaDocTag getTag( String tagName ) {
        return JavaDocSupport.createParamTag( tagName,
                                              parameterComboBox.getEditor().getItem().toString() + " " + // NOI18N
                                              descriptionTextArea.getText() );
    };

