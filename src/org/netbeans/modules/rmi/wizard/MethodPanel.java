    public MethodElement getMethodElement () throws IllegalArgumentException, SourceException {
        return SrcSupport.getRMIMethodElement(me, methodField.getText(), parametersField.getText(), retField.getText());
    }

    public void setMethodElement (MethodElement me) {
        this.me = me;
        methodField.setText(me.getName().toString());
        parametersField.setText(SrcSupport.getParameters(me.getParameters()));
        retField.setText(me.getReturn().toString());
    }

