    void setTexts(String[][] prefil, int selidx) {
        actionComboBox.setEditable(true);
        String[] offer = prefil[0];
        for (int i = 0; i < offer.length; i++) {
            actionComboBox.addItem(offer[i]);
        }
        String txt = prefil[1][0];
        keyTextField.setText(txt);
        if (selidx >= 0) {
            actionComboBox.setSelectedIndex(selidx);
        } else {
            actionComboBox.addItem(""); // NOI18N
            actionComboBox.setSelectedIndex(offer.length);
        }
    }

    String[] getTexts() {
        String[] ret = new String[2];
        ret[0] = (String) actionComboBox.getSelectedItem();
        ret[1] = keyTextField.getText();
        return ret;
    }

