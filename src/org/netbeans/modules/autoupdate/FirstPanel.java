    /** Returns the type of wizard the user selected */
    int getWizardType( ) {
        if ( webRadioButton.isSelected() ) {
            return 0;
        }
        else if ( downloadRadioButton.isSelected() ) {
            return 1;
        }
        return 0;
    }

